// Idioma actual (guardado en localStorage)
let currentLanguage = localStorage.getItem('language') || 'es';

// Función para cambiar idioma
function setLanguage(lang) {
  if(translations[lang]) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);

    // Actualizar el selector
    const selector = document.getElementById('lang-selector');
    if(selector) selector.value = lang;

    updatePageTranslations();
    console.log('✅ Idioma cambiado a:', lang);
  }
}

// Función para obtener una traducción
function t(key) {
  return translations[currentLanguage][key] || translations['es'][key] || key;
}

// Función para actualizar todas las traducciones en la página
function updatePageTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const translation = t(key);

    if(el.tagName === 'INPUT' || el.tagName === 'BUTTON') {
      el.placeholder = translation;
      el.value = translation;
    } else {
      el.textContent = translation;
    }
  });

  // Actualizar atributos alt, title, etc.
  document.querySelectorAll('[data-i18n-attr]').forEach(el => {
    const attr = el.getAttribute('data-i18n-attr');
    const key = el.getAttribute('data-i18n-key');
    const translation = t(key);
    el.setAttribute(attr, translation);
  });

  // Actualizar el idioma del HTML
  document.documentElement.lang = currentLanguage;
}

// Inicializar traducciones cuando carga la página
document.addEventListener('DOMContentLoaded', () => {
  // Actualizar selector al idioma guardado
  const selector = document.getElementById('lang-selector');
  if(selector) selector.value = currentLanguage;

  updatePageTranslations();
  console.log('🌍 Sistema de traducciones inicializado en:', currentLanguage);
});
